static smStatus_t check_object_exists(pSe05xSession_t session, uint32_t object_id, bool *exists)
{
	SE05x_Result_t result = kSE05x_Result_NA;
	smStatus_t sw = Se05x_API_CheckObjectExists(session, object_id, &result);

	if (sw == SM_OK) {
		*exists = (result == kSE05x_Result_SUCCESS);
	}

	return sw;
}

static void prepare_certificate_object(se05x_demo_stats_t *stats, ex_sss_boot_ctx_t *boot_ctx,
				       pSe05xSession_t se_session, sss_object_t *cert_obj)
{
	bool exists = false;
	smStatus_t sw = check_object_exists(se_session, SE05X_DEMO_OBJECT_ID_CERT, &exists);
	sss_status_t status;

	if (sw != SM_OK) {
		se05x_demo_mark_fail_sw(stats, "CheckObjectExists(DEMO_CERT)", sw);
		return;
	}

	LOG_INF("Certificate object_id=0x%08" PRIX32 " exists=%s",
		(uint32_t)SE05X_DEMO_OBJECT_ID_CERT, exists ? "yes" : "no");

	status = sss_key_object_init(cert_obj, &boot_ctx->ks);
	if (status != kStatus_SSS_Success) {
		se05x_demo_mark_fail_status(stats, "sss_key_object_init(DEMO_CERT)", status);
		return;
	}

	if (exists) {
		status = sss_key_object_get_handle(cert_obj, SE05X_DEMO_OBJECT_ID_CERT);
		if (status == kStatus_SSS_Success) {
			se05x_demo_mark_pass(stats, "GetHandle(DEMO_CERT)");
		} else {
			se05x_demo_mark_fail_status(stats, "GetHandle(DEMO_CERT)", status);
		}
		return;
	}

	status = sss_key_object_allocate_handle(cert_obj, SE05X_DEMO_OBJECT_ID_CERT,
					       kSSS_KeyPart_Default,
					       kSSS_CipherType_Binary,
					       sizeof(k_demo_device_cert_der),
					       kKeyObject_Mode_Persistent);
	if (status != kStatus_SSS_Success) {
		se05x_demo_mark_fail_status(stats, "AllocateHandle(DEMO_CERT)", status);
		return;
	}

	status = sss_key_store_set_key(&boot_ctx->ks, cert_obj, k_demo_device_cert_der,
				       sizeof(k_demo_device_cert_der),
				       sizeof(k_demo_device_cert_der) * 8U, NULL, 0);
	if (status == kStatus_SSS_Success) {
		LOG_INF("Certificate persistent object created=yes object_id=0x%08" PRIX32,
			(uint32_t)SE05X_DEMO_OBJECT_ID_CERT);
		se05x_demo_mark_pass(stats, "SetKey(DEMO_CERT)");
	} else {
		se05x_demo_mark_fail_status(stats, "SetKey(DEMO_CERT)", status);
	}
}

static void read_and_verify_certificate(se05x_demo_stats_t *stats,
					ex_sss_boot_ctx_t *boot_ctx,
					sss_object_t *cert_obj)
{
	uint8_t cert[512] = { 0 };
	size_t cert_len = sizeof(cert);
	size_t cert_bit_len = 0;
	sss_status_t status = sss_key_store_get_key(&boot_ctx->ks, cert_obj, cert,
						    &cert_len, &cert_bit_len);

	if (status != kStatus_SSS_Success) {
		se05x_demo_mark_fail_status(stats, "GetKey(DEMO_CERT)", status);
		return;
	}

	LOG_INF("Certificate readback len=%u bit_len=%u", (unsigned int)cert_len,
		(unsigned int)cert_bit_len);
	se05x_demo_log_hex_preview("CertificateDER", cert, cert_len);

	if (cert_len != sizeof(k_demo_device_cert_der) ||
	    memcmp(cert, k_demo_device_cert_der, sizeof(k_demo_device_cert_der)) != 0) {
		LOG_ERR("Certificate object exists but content is not this demo certificate");
		se05x_demo_mark_fail_status(stats, "Compare(DEMO_CERT)", kStatus_SSS_Fail);
		return;
	}

	se05x_demo_mark_pass(stats, "GetKey+Compare(DEMO_CERT)");
}

static sss_status_t run_certificate_store(ex_sss_boot_ctx_t *boot_ctx)
{
	se05x_demo_stats_t stats;
	sss_se05x_session_t *session = (sss_se05x_session_t *)&boot_ctx->session;
	pSe05xSession_t se_session = &session->s_ctx;
	sss_object_t cert_obj = { 0 };

	se05x_demo_stats_init(&stats, "CERTIFICATE_STORE");
	LOG_INF("CERTIFICATE_STORE 开始：写入/复用 demo 设备证书并回读校验");
	LOG_INF("会写 persistent NVM：object_id=0x%08" PRIX32
		"；已有对象时不覆盖",
		(uint32_t)SE05X_DEMO_OBJECT_ID_CERT);

	prepare_certificate_object(&stats, boot_ctx, se_session, &cert_obj);
	if (stats.fail == 0U) {
		read_and_verify_certificate(&stats, boot_ctx, &cert_obj);
	}

	sss_key_object_free(&cert_obj);
	se05x_demo_log_summary(&stats);
	return se05x_demo_stats_result(&stats);
}

