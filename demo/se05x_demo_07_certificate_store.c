/*
 * 07 号业务 demo：CERTIFICATE_STORE，设备证书对象写入和回读校验。
 *
 * 真实业务含义：
 *   量产 provisioning 工站通常会在 SE05x 中保存设备证书、证书链或业务公钥
 *   材料。TLS 客户端认证时，应用从 SE05x 读取证书发给服务器，而私钥仍在
 *   SE05x 内部完成签名。
 *
 * 当前 demo 的边界：
 *   - 会写 SE05x persistent NVM。
 *   - 写入对象：SE05X_DEMO_OBJECT_ID_CERT，也就是 0xEF070001。
 *   - 写入内容：和 06 号 demo 私钥匹配的自签 DER demo 证书，只用于开发验证。
 *   - 如果对象已经存在，本 demo 不覆盖，只回读并和 demo 证书做比较。
 *   - 如果已有对象内容不同，demo 会 FAIL，提醒不要误覆盖未知对象。
 *
 * 执行流程：
 *   1. CheckObjectExists(0xEF070001)：确认 demo 证书对象是否已存在。
 *   2. 不存在时：allocate persistent binary object，并写入 DER demo 证书。
 *   3. 存在时：sss_key_object_get_handle() 获取已有对象，绝不覆盖。
 *   4. sss_key_store_get_key() 回读证书。
 *   5. 对比长度和内容，确认写入/已有对象就是本 demo 期望的证书。
 *
 * 期望串口输出：
 *   - 能看到 cert object_id=0xEF070001。
 *   - 首次运行看到 created=yes；再次运行看到 created=no。
 *   - 能看到 certificate preview 和 cert_len=408。
 *   - 最后应看到 “CERTIFICATE_STORE 汇总：... fail=0”。
 */

